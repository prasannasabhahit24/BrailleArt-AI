"""
evaluation.py

Evaluation Agent for BrailleArt AI.
Responsibility:
Performs multi-modal dual-modality comparison of the generated Braille drawing 
against the original source image or source prompt.
Measures layout fidelity, checks if crucial details are missing or distorted, 
and scores alignment (0.0 to 1.0) with detailed suggestions for improvement.
"""

from typing import List, Dict, Any, Optional
from pydantic import BaseModel, Field
from google import genai
from google.genai import types

class EvaluationInput(BaseModel):
    """Input payload containing original source and generated Braille representation."""
    original_image_bytes: Optional[bytes] = Field(None, description="Original visual image data.")
    original_prompt: str = Field(..., description="Original user prompt or description.")
    generated_braille: str = Field(..., description="The output Braille unicode art generated by tools.")

class FidelityIssue(BaseModel):
    """Specific visual-tactile mismatch details."""
    issue_description: str = Field(..., description="What part of the layout has issues (e.g. circle is squished).")
    severity: str = Field(..., description="Severity of the issue: 'low', 'medium', or 'high'.")
    remediation: str = Field(..., description="Recommended fix (e.g. increase horizontal scale).")

class EvaluationOutput(BaseModel):
    """Report grading the fidelity of the Braille output."""
    fidelity_score: float = Field(..., description="The overall score rating visual-tactile alignment (0.0 to 1.0).")
    issues_found: List[FidelityIssue] = Field(..., description="List of detected alignment issues.")
    summary: str = Field(..., description="A verbal summary of the evaluation.")
    passed: bool = Field(..., description="Whether the output meets the target quality threshold (e.g. score >= 0.8).")

class EvaluationAgent:
    """
    EvaluationAgent class comparing input vs output.
    
    System Prompt:
    You are the Evaluation Agent for BrailleArt AI. Your goal is to critically compare the
    original artwork/image with the generated Braille grid. Grade the layout fidelity.
    Look for squished aspect ratios, missing landmarks, or excessive noise, and report fixes.
    """

    def __init__(self, client: Optional[genai.Client] = None):
        """
        Initialize the Evaluation Agent.
        """
        self._client = client
        self.system_prompt = (
            "You are the Evaluation Agent for BrailleArt AI. Your role is to perform dual-modality "
            "comparisons. You look at both the source (image/text) and the final Braille output, "
            "detecting distortion, aspect ratio warp, or cluttered dot blocks."
        )

    @property
    def client(self) -> genai.Client:
        """Lazily instantiates the GenAI Client when needed."""
        if self._client is None:
            self._client = genai.Client()
        return self._client

    def evaluate_output(self, input_data: EvaluationInput) -> EvaluationOutput:
        """
        Grades visual alignment and checks for distortion.
        
        Args:
            input_data (EvaluationInput): Source data and final drawing.
            
        Returns:
            EvaluationOutput: Evaluation report card.
        """
        # TODO: Send both the image and the generated Braille art to Gemini 2.5 Flash for comparison
        return EvaluationOutput(
            fidelity_score=0.92,
            issues_found=[],
            summary="The circle shape matches the centering and dimensions of the source image perfectly.",
            passed=True
        )
